<template>
  <div />
</template>

<script>
export default {
  async mounted() {
    let data;
    do {
      if (data) {
        // eslint-disable-next-line no-await-in-loop
        await this.$store.dispatch('modals/open', {
          name: 'alert',
          text: `
            Scanned QR code is wrong.
            Please make sure to scan a QR code generated by the Base Ã¦pp
          `,
        });
      }

      try {
        // eslint-disable-next-line no-await-in-loop
        data = await this.$store.dispatch('modals/open', {
          name: 'readQrCode',
          title: 'Remote Connection',
        });
      } catch (e) {
        this.$router.push({ name: 'settings-remote-connection' });
        return;
      }

      data = Buffer.from(data, 'base64');
    } while (data.length !== 15);

    this.$store.commit('addFollower', {
      id: data.toString('base64'),
      name: prompt('Name this device') || 'Unnamed',
    });
    this.$router.push({ name: 'settings-remote-connection' });
  },
};
</script>
